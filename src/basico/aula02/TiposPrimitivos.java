package basico.aula02;

public class TiposPrimitivos {
    public static void main(String[] args) {
        int inteiro = 10;
        String text = "eu sou um texto";
        char caracter = 'A';
        double doble64 = 45.23;
        float flutuante = 25.232f;
        long longo = 1000000000000000000l;
        boolean verdadeiroOuFalso = true;

        System.out.println("inteiro int = " + inteiro);
        System.out.println("string texto = " + text);
        System.out.println("caracter char = " + caracter);
        System.out.println("double doble64 = " + doble64);
        System.out.println("float flutuante = " + flutuante);
        System.out.println("long longo = " + longo);
        System.out.println("boolean verdadeiroOuFalso = " + verdadeiroOuFalso);
    }
}
