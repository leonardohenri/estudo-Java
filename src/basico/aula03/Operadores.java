package basico.aula03;

public class Operadores {
    public static void main(String[] args){
        int number1 = 10;
        double number2 = 2.4;

        System.out.println("soma: "+(number1+number2));
        System.out.println("subtração: "+(number1-number2));
        System.out.println("multiplicação: "+(number1*number2));
        System.out.println("divisão: "+(number1/number2));
        System.out.println("comparação: "+(number1==number2));
        System.out.println("maior igual: "+(number1>=number2));
        System.out.println("diferente: "+(number1!=number2));
    }
}
